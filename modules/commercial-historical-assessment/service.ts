export async function generateAssessment(role: string, tenantKey: string, userId: string, id: string) {
  checkHistoricalAssessmentPermission(role, 'assessment:generate');
  const run = await repo.startGeneration(tenantKey, id);
  try {
    const provider = await resolveUserLLMProvider(userId);
    const status = provider.getStatus();
    const response = await provider.generate(buildPrompt(run), {
      systemPrompt: SYSTEM_PROMPT,
      maxTokens: 1800,
      temperature: 0.15,
      timeoutMs: 45000,
    });
    const generated = generatedAssessmentSchema.parse(parseJsonResponse(response.text));
    const saved = await repo.saveGeneratedFindings(tenantKey, userId, id, generated, {
      type: response.provider || status.type,
      model: response.model || status.model || null,
    });
    auditLog(
      { actor: `user:${userId}`, action: 'historical_assessment_ai_generated', object_type: 'commercial_historical_assessment_run', object_id: id, result: 'success' },
      `Historical assessment generated by ${response.provider || status.type}`,
    );
    return repo.serializeAssessment(saved);
  } catch (error) {
    const reason = safeFailureReason(error);
    await repo.markGenerationFailed(tenantKey, id, reason);
    auditLog(
      { actor: `user:${userId}`, action: 'historical_assessment_ai_generation_failed', object_type: 'commercial_historical_assessment_run', object_id: id, result: 'failure' },
      reason,
    );
    throw error;
  }
}

