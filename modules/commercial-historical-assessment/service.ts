function buildPrompt(run: Awaited<ReturnType<typeof repo.startGeneration>>): string {
  const evidence = run.evidence.slice(0, 120).map(item => ({
    id: item.id,
    type: item.evidence_type,
    sourceName: item.source_name,
    metricKey: item.metric_key,
    metricValue: item.metric_value,
    metricUnit: item.metric_unit,
    observedAt: item.observed_at,
    payload: item.payload,
  }));
  return JSON.stringify({
    task: 'Analyze historical commercial performance and propose evidence-backed learning for future planning.',
    outputSchema: {
      findings: [{
        type: 'repeat | improve | avoid | investigate',
        title: 'short title',
        summary: 'what the evidence shows',
        recommendation: 'what future planning should do',
        confidence: 'number from 0 to 1',
        evidenceIds: ['one or more IDs from evidence'],
      }],
    },
    assessment: {
      id: run.id,
      title: run.title,
      dateFrom: run.date_from,
      dateTo: run.date_to,
      evidenceSummary: run.evidence_summary,
      knownMissingData: run.missing_data,
      evidenceIncluded: evidence.length,
      evidenceTotal: run.evidence.length,
    },
    evidence,
  });
}

function parseJsonResponse(text: string): unknown {
  const trimmed = text.trim();
  const fenced = /^```(?:json)?\s*([\s\S]*?)\s*```$/i.exec(trimmed);
  const candidate = fenced?.[1] || trimmed;
  const start = candidate.indexOf('{');
  const end = candidate.lastIndexOf('}');
  if (start < 0 || end <= start) {
    throw new AppError('AI returned no valid assessment JSON', 502, 'AI_ASSESSMENT_INVALID_RESPONSE');
  }
  try {
    return JSON.parse(candidate.slice(start, end + 1));
  } catch {
    throw new AppError('AI returned malformed assessment JSON', 502, 'AI_ASSESSMENT_INVALID_RESPONSE');
  }
}

function safeFailureReason(error: unknown): string {
  if (error instanceof AppError) return error.message;
  return error instanceof Error && error.message
    ? `Assessment generation failed: ${error.message.slice(0, 700)}`
    : 'Assessment generation failed because the AI provider returned an unexpected error.';
}
