import { AppError } from '@shared/errors';
import { auditLog } from '@shared/logging';
import { resolveUserLLMProvider } from '@modules/ai-provider/controller';
import { checkHistoricalAssessmentPermission } from './policy';
import * as repo from './repository';
import {
  generatedAssessmentSchema,
  type AssessmentScopeInput,
  type CreateAssessmentRunInput,
  type DecideAssessmentFindingInput,
  type ListAssessmentRunsInput,
} from './types';

const SYSTEM_PROMPT = [
  'You are Stitchi, Tanaghum\'s evidence-grounded commercial assessment analyst.',
  'Analyze only the supplied immutable evidence snapshot.',
  'Never invent sales, spend, reach, leads, purchases, audience behavior, or success criteria.',
  'Every finding must cite one or more supplied evidence IDs.',
  'Use Repeat for a proven strength, Improve for an underperforming area, Avoid for a harmful pattern, and Investigate when evidence is incomplete or contradictory.',
  'Return JSON only, matching the requested schema. Do not include markdown fences.',
].join(' ');

export async function previewAssessment(role: string, tenantKey: string, input: AssessmentScopeInput) {
  checkHistoricalAssessmentPermission(role, 'assessment:read');
  return repo.previewEvidence(tenantKey, input);
}

export async function createAssessment(
  role: string,
  tenantKey: string,
  userId: string,
  input: CreateAssessmentRunInput,
) {
  checkHistoricalAssessmentPermission(role, 'assessment:create');
  const preview = await repo.previewEvidence(tenantKey, input);
  const run = await repo.createAssessmentRun(tenantKey, userId, input, preview);
  auditLog(
    { actor: `user:${userId}`, action: 'historical_assessment_created', object_type: 'commercial_historical_assessment_run', object_id: run.id, result: 'success' },
    `Historical assessment created with ${preview.evidence.length} evidence record(s)`,
  );
  return repo.serializeAssessment(run);
}

